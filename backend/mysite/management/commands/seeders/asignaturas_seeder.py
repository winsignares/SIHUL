from asignaturas.models import Asignatura


def create_asignaturas(stdout, style):
    """Crear todas las asignaturas de todos los programas"""
    stdout.write('  → Creando asignaturas...')
    stdout.write('     Este proceso puede tomar unos segundos...')
    
    asignaturas_data = _get_all_asignaturas()
    
    created_count = 0
    updated_count = 0
    
    for nombre, codigo, creditos, tipo_str, horas in asignaturas_data:
        # Mapear tipo_str al valor del modelo
        tipo_map = {
            'Básica': Asignatura.BASICA,
            'Profesional': Asignatura.PROFESIONAL,
            'Humanística': Asignatura.HUMANISTICA,
            'Práctico': Asignatura.PRACTICO,
            'Optativa': Asignatura.OPTATIVA,
            'Electiva': Asignatura.ELECTIVA,
        }
        tipo = tipo_map.get(tipo_str, Asignatura.PROFESIONAL)
        
        asignatura, created = Asignatura.objects.update_or_create(
            codigo=codigo,
            defaults={
                'nombre': nombre,
                'creditos': creditos,
                'tipo': tipo,
                'horas': horas
            }
        )
        
        if created:
            created_count += 1
        else:
            updated_count += 1
    
    total = len(asignaturas_data)
    stdout.write(style.SUCCESS(f'    ✓ Asignaturas procesadas: {created_count} nuevas, {updated_count} actualizadas, {total} totales'))


